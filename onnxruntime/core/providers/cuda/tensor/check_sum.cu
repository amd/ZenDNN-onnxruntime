__global__ void vector_checksum_crc(const void* array, int nbytes, uint32_t& result, uint32_t sum = 0xffffffff) {
  __syncthreads();
  if (threadIdx.x == 0 && blockIdx.x == 0) {
    const unsigned char* array_ptr = reinterpret_cast<const unsigned char*>(array);
    sum = crc_update(sum, array_ptr, nbytes);
    sum = crc_finalize(sum);
    result = sum;
  } else {
    result = 0xffffffff;
  }
  __syncthreads();
}

// Coded for m x n column major arrays with column stride lda
__device__ uint32_t array_checksum_crc(const float* A, int m, int n, int lda, uint32_t sum = 0xffffffff) {
  __syncthreads();
  if (threadIdx.x == 0 && blockIdx.x == 0) {
    for (int i = 0; i < n; i++) {
      const unsigned char* array_ptr = (const unsigned char*)A;
      sum = crc_update(sum, array_ptr, m * sizeof(float));
    }
    sum = crc_finalize(sum);
    return sum;
  } else {
    return 0xffffffff;
  }
  __syncthreads();
}

uint32_t CheckSumImpl(cudaStream_t stream,
                      const void* input_data,
                      int nbytes) {
  uint32_t result = 0;
  vector_checksum_crc<<<1, 1, 0, stream>>>(input_data, nbytes, result);
  return result;
}

