__device__ uint32_t array_checksum_xor(const float* A, int m, int n, int lda, uint32_t prevValue = 0x00000000) {
  uint32_t sum = prevValue;
  __syncthreads();
  if (threadIdx.x == 0 && blockIdx.x == 0) {
